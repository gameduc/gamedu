window.triviaQuestions = [];
window.activeTriviaQuestionId = null;
window.currentTriviaSetId = null; // Mevcut seti düzenlemek için takip ID'si

const TriviaEditor = {
    isInitialized: false,
    init: function() {
        if(this.isInitialized) return;
        this.isInitialized = true;
        console.log("GamEdu Trivia Editor Initialized");
        this.bindEvents();
    },

    bindEvents: function() {
        // Yeni Soru Ekle
        const addBtn = document.getElementById('triviaAddNewQuestionBtn');
        if (addBtn) addBtn.addEventListener('click', () => this.addNewQuestion());

        // Tip Değiştirme
        const typeSelect = document.getElementById('triviaQTypeSelect');
        if (typeSelect) typeSelect.addEventListener('change', (e) => this.renderDynamicFields(e.target.value));

        // Metin Güncelleme
        const textInput = document.getElementById('triviaQTextInput');
        if (textInput) textInput.addEventListener('input', (e) => {
            if (window.activeTriviaQuestionId) {
                const q = this.getQ(window.activeTriviaQuestionId);
                if(q) q.text = e.target.value;
                this.renderQuestionsList();
            }
        });

        // 2x Puan Güncelleme
        const dpInput = document.getElementById('triviaQDoublePoints');
        if (dpInput) dpInput.addEventListener('change', (e) => {
            if (window.activeTriviaQuestionId) {
                const q = this.getQ(window.activeTriviaQuestionId);
                if(q) q.doublePoints = e.target.checked;
                this.renderQuestionsList();
            }
        });

        // Süre (sn) Güncelleme
        const timeInput = document.getElementById('triviaQTimeInput');
        if (timeInput) timeInput.addEventListener('input', (e) => {
            if (window.activeTriviaQuestionId) {
                const q = this.getQ(window.activeTriviaQuestionId);
                const t = parseInt(e.target.value) || 20;
                if(q) q.timeLimit = t;
            }
        });

        // Resim URL
        const imgInput = document.getElementById('triviaQImageInput');
        if(imgInput) imgInput.addEventListener('input', (e) => {
            if (window.activeTriviaQuestionId) {
                const q = this.getQ(window.activeTriviaQuestionId);
                if(q) {
                    q.imageUrl = e.target.value;
                    document.getElementById('triviaQImagePreview').style.backgroundImage = `url('${q.imageUrl}')`;
                }
            }
        });

        // Kaydet Butonu
        const saveBtn = document.getElementById('triviaSaveSetBtn');
        if(saveBtn) saveBtn.addEventListener('click', () => this.saveSetToFirebase());
    },

    getQ: function(id) {
        return window.triviaQuestions.find(q => q.id === id);
    },

    addNewQuestion: function() {
        const newQ = {
            id: 'q_' + Date.now(),
            type: 'mcq',
            text: 'Yeni Soru',
            imageUrl: '',
            doublePoints: false,
            timeLimit: 20, // Varsayılan süre 20 saniye
            answers: [
                { text: 'A Şıkkı', isCorrect: true },
                { text: 'B Şıkkı', isCorrect: false },
                { text: 'C Şıkkı', isCorrect: false },
                { text: 'D Şıkkı', isCorrect: false }
            ] // Varsayılan mcq verisi
        };
        window.triviaQuestions.push(newQ);
        this.renderQuestionsList();
        this.openQuestion(newQ.id);
    },

    deleteQuestion: function(id) {
        showOzelAlert("Bu soruyu silmek istediğinize emin misiniz?", "evethayir", (isConfirmed) => {
            if (isConfirmed) {
                window.triviaQuestions = window.triviaQuestions.filter(q => q.id !== id);
                if (window.activeTriviaQuestionId === id) {
                    this.showEmptyState();
                }
                this.renderQuestionsList();
            }
        });
    },

    showEmptyState: function() {
        window.activeTriviaQuestionId = null;
        document.getElementById('triviaQuestionEditorArea').style.display = 'none';
        document.getElementById('triviaNoQuestionSelected').style.display = 'flex';
    },

    renderQuestionsList: function() {
        const listDiv = document.getElementById('triviaQuestionsList');
        if(!listDiv) return;
        
        listDiv.innerHTML = '';
        if (window.triviaQuestions.length === 0) {
            listDiv.innerHTML = '<div style="color:#64748b; text-align:center; padding:20px; font-size:0.9rem;">Soru eklenmedi.</div>';
            return;
        }

        window.triviaQuestions.forEach((q, index) => {
            const isOtoMCQ = q.autoGenerated ? '<span style="background:#eab308; color:#000; padding:2px 4px; border-radius:3px; font-size:0.7rem; font-weight:bold; margin-left:5px;">Oto-MCQ</span>' : '';
            const isDP = q.doublePoints ? '<span style="background:#f59e0b; color:#fff; padding:2px 4px; border-radius:3px; font-size:0.7rem; font-weight:bold; margin-left:5px;">2x Puan</span>' : '';
            const isActive = q.id === window.activeTriviaQuestionId ? 'background: rgba(139, 92, 246, 0.2); border-color: #8b5cf6;' : 'background: rgba(255,255,255,0.05); border-color: transparent;';
            
            const card = document.createElement('div');
            card.style = `padding: 10px; border-radius: 8px; border: 1px solid transparent; cursor: pointer; display: flex; align-items: center; justify-content: space-between; transition: 0.2s; ${isActive}`;
            
            let shortText = q.text.length > 25 ? q.text.substring(0, 25) + "..." : q.text;
            if(!shortText.trim()) shortText = "Boş Soru";
            
            card.innerHTML = `
                <div style="flex:1; overflow:hidden;" onclick="TriviaEditor.openQuestion('${q.id}')">
                    <div style="font-size: 0.8rem; color: #cbd5e1; margin-bottom: 3px;">Soru ${index + 1} ${isOtoMCQ} ${isDP}</div>
                    <div style="font-size: 0.95rem; color: #fff; white-space: nowrap; overflow: hidden; text-overflow: ellipsis;">${shortText}</div>
                </div>
                <button onclick="TriviaEditor.deleteQuestion('${q.id}')" style="background:transparent; border:none; color:#ef4444; cursor:pointer; font-size:1.2rem; padding:5px; margin-left:10px;">🗑</button>
            `;
            listDiv.appendChild(card);
        });
    },

    openQuestion: function(id) {
        window.activeTriviaQuestionId = id;
        this.renderQuestionsList(); // Seçimi highlightla
        
        const q = this.getQ(id);
        if(!q) return;

        document.getElementById('triviaNoQuestionSelected').style.display = 'none';
        document.getElementById('triviaQuestionEditorArea').style.display = 'block';

        document.getElementById('triviaQTypeSelect').value = q.type;
        document.getElementById('triviaQTextInput').value = q.text;
        document.getElementById('triviaQDoublePoints').checked = !!q.doublePoints;
        document.getElementById('triviaQTimeInput').value = q.timeLimit || 20;
        document.getElementById('triviaQImageInput').value = q.imageUrl || '';
        document.getElementById('triviaQImagePreview').style.backgroundImage = q.imageUrl ? `url('${q.imageUrl}')` : 'none';

        this.renderDynamicFields(q.type);
    },

    renderDynamicFields: function(type) {
        const area = document.getElementById('triviaDynamicFieldsArea');
        if(!area) return;
        area.innerHTML = '';
        const q = this.getQ(window.activeTriviaQuestionId);
        if(!q) return;

        // Tipi değiştiyse veriyi sıfırla/dönüştür (Basitlik için sıfırlıyoruz)
        if (q.type !== type) {
            q.type = type;
            if (type === 'mcq') q.answers = [{ text: 'A', isCorrect: true }, { text: 'B', isCorrect: false }, { text: 'C', isCorrect: false }, { text: 'D', isCorrect: false }];
            else if (type === 'tf') q.answers = [{ text: 'Doğru', isCorrect: true }, { text: 'Yanlış', isCorrect: false }];
            else if (type === 'type') q.answers = [{ text: '', isCorrect: true }];
        }

        if (type === 'mcq') {
            area.innerHTML = `<h4 style="color:#cbd5e1; margin-top:0; margin-bottom:15px;">Şıklar (Doğru cevabı yeşil yuvarlaktan seçin)</h4>`;
            q.answers.forEach((ans, i) => {
                const color = ans.isCorrect ? '#10b981' : '#475569';
                const div = document.createElement('div');
                div.style = 'display:flex; align-items:center; gap:10px; margin-bottom:10px;';
                div.innerHTML = `
                    <div style="width:24px; height:24px; border-radius:50%; background:${color}; border:2px solid #fff; cursor:pointer;" onclick="TriviaEditor.setMcqCorrect(${i})"></div>
                    <input type="text" value="${ans.text}" oninput="TriviaEditor.updateAnswerText(${i}, this.value)" style="flex:1; padding:10px; background:rgba(0,0,0,0.4); border:1px solid #475569; color:#fff; border-radius:5px;">
                `;
                area.appendChild(div);
            });
        } 
        else if (type === 'tf') {
            area.innerHTML = `<h4 style="color:#cbd5e1; margin-top:0; margin-bottom:15px;">Doğru / Yanlış İfadesi</h4>`;
            q.answers.forEach((ans, i) => {
                const color = ans.isCorrect ? '#10b981' : '#475569';
                const div = document.createElement('div');
                div.style = 'display:flex; align-items:center; gap:10px; margin-bottom:10px;';
                div.innerHTML = `
                    <div style="width:24px; height:24px; border-radius:50%; background:${color}; border:2px solid #fff; cursor:pointer;" onclick="TriviaEditor.setTfCorrect(${i})"></div>
                    <div style="flex:1; padding:10px; background:rgba(0,0,0,0.4); border:1px solid #475569; color:#fff; border-radius:5px; font-weight:bold;">${ans.text}</div>
                `;
                area.appendChild(div);
            });
        }
        else if (type === 'type') {
            area.innerHTML = `
                <h4 style="color:#cbd5e1; margin-top:0; margin-bottom:10px;">Doğru Metin/Cevap</h4>
                <p style="font-size:0.8rem; color:#94a3b8; margin-bottom:15px;">Öğrenci bu metni klavyeden yazarak cevaplayacaktır. (Büyük/küçük harf duyarlılığı yoktur, kelime/cümle olabilir).</p>
                <input type="text" value="${q.answers[0].text}" oninput="TriviaEditor.updateAnswerText(0, this.value)" placeholder="Tam olarak yazılması beklenen cevap" style="width:100%; padding:10px; background:rgba(0,0,0,0.4); border:1px solid #475569; color:#fff; border-radius:5px;">
            `;
        }
        else {
            area.innerHTML = `<div style="color:#eab308; padding:20px; text-align:center; border:1px dashed #eab308; border-radius:8px;">${type.toUpperCase()} soru türü yapım aşamasındadır. Şimdilik Çoktan Seçmeli, D/Y ve Yazma türlerini kullanabilirsiniz.</div>`;
        }
    },

    setMcqCorrect: function(idx) {
        const q = this.getQ(window.activeTriviaQuestionId);
        if(!q) return;
        q.answers.forEach((a, i) => a.isCorrect = (i === idx));
        this.renderDynamicFields('mcq');
    },

    setTfCorrect: function(idx) {
        const q = this.getQ(window.activeTriviaQuestionId);
        if(!q) return;
        q.answers.forEach((a, i) => a.isCorrect = (i === idx));
        this.renderDynamicFields('tf');
    },

    updateAnswerText: function(idx, val) {
        const q = this.getQ(window.activeTriviaQuestionId);
        if(q && q.answers[idx]) q.answers[idx].text = val;
    },

    saveSetToFirebase: function() {
        if(window.triviaQuestions.length === 0) {
            showOzelAlert("Boş seti kaydedemezsiniz. Lütfen en az bir soru ekleyin.", "hata");
            return;
        }

        const title = document.getElementById('triviaSetNameInput').value.trim();
        if(!title) {
            showOzelAlert("Lütfen setinize bir isim verin.", "hata");
            return;
        }

        if(typeof currentUser === 'undefined' || !currentUser) {
            showOzelAlert("Giriş yapmadan seti kaydedemezsiniz.", "hata");
            return;
        }

        // Validate Questions (Basic)
        let hasError = false;
        window.triviaQuestions.forEach((q, i) => {
            if(!q.text.trim()) hasError = true;
            if(q.type === 'type' && (!q.answers[0] || !q.answers[0].text.trim())) hasError = true;
            if(q.type === 'mcq') {
                q.answers.forEach(a => { if(!a.text.trim()) hasError = true; });
                if(!q.answers.some(a=>a.isCorrect)) hasError = true;
            }
        });

        if(hasError) {
            showOzelAlert("Bazı soruların metni veya cevapları boş görünüyor. Lütfen tüm alanları doldurun.", "hata");
            return;
        }

        const saveData = {
            Title: title,
            Type: "trivia",
            Author_ID: currentUser.uid,
            Author_Name: currentUser.displayName || currentUser.email || "Öğretmen",
            Timestamp: firebase.database.ServerValue.TIMESTAMP,
            IsPublic: true,
            GlobalLevel: "Tümü", // Şimdilik varsayılan eklendi
            GlobalClass: "Tümü",
            GlobalLesson: "Tümü",
            Questions: window.triviaQuestions
        };

        if (window.currentTriviaSetId) {
            // MEVCUT SETİ GÜNCELLE
            database.ref('MasterPool/' + window.currentTriviaSetId).update(saveData)
                .then(() => {
                    showOzelAlert("Trivia seti başarıyla güncellendi!", "bilgi", () => {
                        document.getElementById('triviaEditorModal').classList.remove('active');
                        window.currentTriviaSetId = null;
                        
                        // Veritabanını anlık yenileyerek lobiyi güncelle
                        if (typeof loadGameSetup === 'function' && typeof currentLoadedGame !== 'undefined') {
                            loadGameSetup(currentLoadedGame, 'Local');
                        } else {
                            const st = document.getElementById('SetPreference');
                            if(st) { st.value = "Benim Setlerim"; const ev = new Event('change'); st.dispatchEvent(ev); }
                        }
                    });
                })
                .catch(err => {
                    showOzelAlert("Güncelleme sırasında hata oluştu: " + err.message, "hata");
                });
        } else {
            // YENİ SET OLUŞTUR
            const newSetRef = database.ref('MasterPool').push();
            newSetRef.set(saveData)
                .then(() => {
                    showOzelAlert("Trivia seti başarıyla kaydedildi! Lobide 'Set Havuzu'nu Tümü yaparak veya Benim Setlerimden seçerek hemen oynayabilirsiniz.", "bilgi", () => {
                        document.getElementById('triviaEditorModal').classList.remove('active');
                        window.currentTriviaSetId = null;
                        
                        // Veritabanını anlık yenileyerek lobiyi güncelle
                        if (typeof loadGameSetup === 'function' && typeof currentLoadedGame !== 'undefined') {
                            loadGameSetup(currentLoadedGame, 'Local');
                        } else {
                            const st = document.getElementById('SetPreference');
                            if(st) { st.value = "Benim Setlerim"; const ev = new Event('change'); st.dispatchEvent(ev); }
                        }
                    });
                })
                .catch(err => {
                    showOzelAlert("Kayıt sırasında hata oluştu: " + err.message, "hata");
                });
        }
    }
};

window.openTriviaEditorForSet = function(setId, setObj) {
    if (!setObj || !setObj.Questions) {
        showOzelAlert("Bu sette hiç soru bulunamadı veya formatı hatalı.", "hata");
        return;
    }
    
    window.currentTriviaSetId = setId;
    document.getElementById('triviaSetNameInput').value = setObj.Title || "İsimsiz Set";
    window.triviaQuestions = setObj.Questions;
    
    document.getElementById('triviaEditorModal').classList.add('active');
    TriviaEditor.init();
    
    if(window.triviaQuestions.length > 0) {
        TriviaEditor.renderQuestionsList();
        // İlk soruyu aç
        TriviaEditor.openQuestion(window.triviaQuestions[0].id);
    } else {
        TriviaEditor.showEmptyState();
    }
};

window.initTriviaEditor = function() {
    TriviaEditor.init();
};

window.generateTriviaFromWordspool = function(setId) {
    if (typeof database === 'undefined') {
        showOzelAlert("Veritabanı bağlantısı yok.", "hata");
        return;
    }
    
    // Yükleme mesajını native bir Toast var ise onla yoksa sessiz gecip Modal ile bekletebiliriz..
    // Hızlı geçekleştiğinden direk cekelim
    database.ref('MasterPool').child(setId).once('value').then(snap => {
        if (!snap.exists()) {
            showOzelAlert("Seçilen set bulunamadı.", "hata");
            return;
        }

        const setObj = snap.val();
        let dataList = setObj.Data || [];
        
        // Dict-Object Check (Eğer dizi degil obje kaydedildiyse map'le)
        if(!Array.isArray(dataList)) {
            dataList = Object.values(dataList);
        }

        if (dataList.length < 4) {
            showOzelAlert("Oto üretici için bu sette en az 4 dolu kelime bulunmalıdır.", "hata");
            return;
        }

        const newQuestions = [];

        if (setObj.Type === 'wordspool') {
            dataList.forEach((item, index) => {
                if(!item.Word || !item.MeaningTR) return;
                
                let answers = [{ text: item.MeaningTR, isCorrect: true }];
                let usedIndexes = [index];
                while (answers.length < 4 && usedIndexes.length < dataList.length) {
                    let rnd = Math.floor(Math.random() * dataList.length);
                    if (!usedIndexes.includes(rnd) && dataList[rnd].MeaningTR && dataList[rnd].MeaningTR.trim() !== "") {
                        answers.push({ text: dataList[rnd].MeaningTR, isCorrect: false });
                        usedIndexes.push(rnd);
                    } else {
                        usedIndexes.push(rnd); // Aynı gelirse diye bloğu ilerletmek icin
                    }
                }

                // Shuffle
                answers.sort(() => Math.random() - 0.5);

                newQuestions.push({
                    id: 'q_' + Date.now() + '_' + Math.floor(Math.random()*1000),
                    type: 'mcq',
                    text: `${item.Word}`,
                    imageUrl: item.ImgURL || '',
                    answers: answers,
                    autoGenerated: true
                });
            });
        } 
        else if (setObj.Type === 'qpool') {
            dataList.forEach((item, index) => {
                if(!item.Question && !item.Soru) return;
                const questionText = item.Question || item.Soru || "Boş Soru";
                const img = item.ImageUrl || item.ImgURL || item.Resim || '';
                
                if (setObj.SubType === 'acik_uclu') {
                    newQuestions.push({
                        id: 'q_' + Date.now() + '_' + Math.floor(Math.random()*1000),
                        type: 'type',
                        text: questionText,
                        imageUrl: img,
                        answers: [{ text: item.CorrectAnswer || item.Cevap || '', isCorrect: true }],
                        autoGenerated: true
                    });
                } else {
                    let answers = [];
                    // Şıkları topla
                    if (item.Option_A || item.A) answers.push({ text: item.Option_A || item.A, isCorrect: (item.CorrectAnswer === 'A' || item.Cevap === 'A') });
                    if (item.Option_B || item.B) answers.push({ text: item.Option_B || item.B, isCorrect: (item.CorrectAnswer === 'B' || item.Cevap === 'B') });
                    if (item.Option_C || item.C) answers.push({ text: item.Option_C || item.C, isCorrect: (item.CorrectAnswer === 'C' || item.Cevap === 'C') });
                    if (item.Option_D || item.D) answers.push({ text: item.Option_D || item.D, isCorrect: (item.CorrectAnswer === 'D' || item.Cevap === 'D') });
                    if (item.Option_E || item.E) answers.push({ text: item.Option_E || item.E, isCorrect: (item.CorrectAnswer === 'E' || item.Cevap === 'E') });
                    
                    if (answers.length === 0) {
                        answers = [{ text: item.CorrectAnswer || item.Cevap || "Doğru", isCorrect: true }];
                    }

                    newQuestions.push({
                        id: 'q_' + Date.now() + '_' + Math.floor(Math.random()*1000),
                        type: 'mcq',
                        text: questionText,
                        imageUrl: img,
                        answers: answers,
                        autoGenerated: true
                    });
                }
            });
        }

        if(newQuestions.length === 0) {
            showOzelAlert("Bu set içerisinden uygun soru çıkarılamadı (Kelimelerin Türkçe anlamları boş olabilir).", "hata");
            return;
        }

        document.getElementById('triviaSetNameInput').value = (setObj.Title || "Wordspool Dönüşüm") + " (Oto-Quiz)";
        window.triviaQuestions = newQuestions;
        window.currentTriviaSetId = null; // Oto-quiz yeni set demektir
        
        document.getElementById('triviaEditorModal').classList.add('active');
        TriviaEditor.init();
        TriviaEditor.showEmptyState();
        TriviaEditor.renderQuestionsList();
        
    }).catch(err => {
        showOzelAlert("Hata oluştu: " + err.message, "hata");
    });
};
